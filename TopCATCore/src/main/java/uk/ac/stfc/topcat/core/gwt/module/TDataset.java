package uk.ac.stfc.topcat.core.gwt.module;

import java.io.Serializable;

/**
 * This is shared with GWT for dataset information. This is temporary storage
 * for dataset information that will be passed to topcat.
 * <p>
 * 
 * @author Mr. Srikanth Nagella
 * @version 1.0, &nbsp; 30-APR-2010
 * @since iCAT Version 3.3
 */
public class TDataset implements Serializable {
    private static final long serialVersionUID = 1L;
    private String facilityName;
    private String investigationId;
    private String id;
    private String name;
    private String status;
    private String type;
    private String description;
    private String sample;

    public TDataset() {
    }

    public TDataset(String facilityName, String id) {
        this.facilityName = facilityName;
        this.id = id;
    }

    public TDataset(String facilityName, String investigationId, String id, String name, String description,
            String type, String status) {
        this.facilityName = facilityName;
        this.investigationId = investigationId;
        this.id = id;
        this.name = name;
        this.description = description;
        this.type = type;
        this.status = status;
    }

    public String getFacilityName() {
        return facilityName;
    }

    public void setFacilityName(String facilityName) {
        this.facilityName = facilityName;
    }

    public String getInvestigationId() {
        return investigationId;
    }

    public void setInvestigationId(String investigationId) {
        this.investigationId = investigationId;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSample() {
        return sample;
    }

    public void setSample(String sample) {
        this.sample = sample;
    }

}
