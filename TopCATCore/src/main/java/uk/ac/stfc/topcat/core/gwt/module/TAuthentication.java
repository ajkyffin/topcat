package uk.ac.stfc.topcat.core.gwt.module;

import java.io.Serializable;

/**
 * This is shared with GWT for authentication information.
 * 
 */
public class TAuthentication implements Serializable {
    private static final long serialVersionUID = 1L;
    private String displayName;
    private String facilityName;
    private String pluginName;
    private String url;
    private String type;
    private Long id;

    public TAuthentication() {
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getFacilityName() {
        return facilityName;
    }

    public void setFacilityName(String facilityName) {
        this.facilityName = facilityName;
    }

    public String getPluginName() {
        return pluginName;
    }

    public void setPluginName(String pluginName) {
        this.pluginName = pluginName;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public String toString() {
        StringBuilder message = new StringBuilder();
        message.append("TAuthentication\n");
        if (id != null) {
            message.append("id:").append(id).append("\n");
        }
        message.append("displayName:").append(displayName).append("\n");
        message.append("facilityName:").append(facilityName).append("\n");
        message.append("pluginName:").append(pluginName).append("\n");
        message.append("url:").append(url).append("\n");
        message.append("type:").append(type);
        return message.toString();
    }
}
